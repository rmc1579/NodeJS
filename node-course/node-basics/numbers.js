var age = 36;
var results;

console.log('age = ' + age);

results = age + 6;
console.log('age + 6 = ' + results);

results = age - 1;
console.log('age - 1 = ' + results);

results = age * 3;
console.log('age * 3 = ' + results);

results = age / 6;
console.log('age / 6 = ' + results);

results = age % 5;
console.log('age % 5 = ' + results);

results--;
console.log('minus - 1 = ' + results);

results++;
console.log('plus 1 = ' + results);

